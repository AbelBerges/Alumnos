    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean activo = false;
        temp.setNombre(jtAltaNombre.getText());
        temp.setApellido(jtAltaApeliido.getText());
        temp.setDni(Integer.parseInt(jtAltaDNI.getText()));
        temp.setFechaNacimiento(LocalDate.parse(jtAltaFechaNac.getText()));
        int valorActivo = Integer.parseInt(jtAltaEstado.getText());
        if (valorActivo == 1) {
            activo = true;
        }

        temp.setEstado(activo);
        mod.insertoDatos(temp);
        armoFilas();
        jtAltaNombre.setText("");
        jtAltaApeliido.setText("");
        jtAltaFechaNac.setText("");
        jtAltaEstado.setText("");
        jtAltaDNI.setText("");
        jtAltaNombre.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

