    private void jtEdicionBusquedaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtEdicionBusquedaKeyReleased
        ResultSet seleccion = mod.selecAlumno();
        borrarFilas();
        try {
            while (seleccion.next()) {
                if (jtEdicionBusqueda.getText().isEmpty()) {
                    borrarFilas();
                } else if (seleccion.getString("nombre").startsWith(jtEdicionBusqueda.getText())
                        || seleccion.getString("apellido").startsWith(jtEdicionBusqueda.getText())) {
                    edicion.addRow(new Object[]{seleccion.getInt(1), seleccion.getInt(2), seleccion.getString("apellido"),
                        seleccion.getString("nombre"), seleccion.getString("fechaNacimiento"),
                        seleccion.getString("estado")});
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al consultar la base de datos");
        }
    }//GEN-LAST:event_jtEdicionBusquedaKeyReleased

