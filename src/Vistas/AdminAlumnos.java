    private void armoTablaAlta() {
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("DNI");
        modelo.addColumn("Fecha de Nacimiento");
        modelo.addColumn("Activo");
        jTablaAlta.setModel(modelo);
    }

    private void armoFilas() {
        modelo.addRow(new Object[]{jtAltaNombre.getText(), jtAltaApeliido.getText(), jtAltaDNI.getText(), jtAltaFechaNac.getText(),
            jtAltaEstado.getText()});
    }

    private void armoTablaEdicion() {
        edicion.addColumn("Identificador");
        edicion.addColumn("Nombre");
        edicion.addColumn("Apellido");
        edicion.addColumn("DNI");
        edicion.addColumn("Fecha de Nacimiento");
        edicion.addColumn("Estado");
        jTablaResultado.setModel(edicion);
    }

    private void borrarFilas() {
        int filas = edicion.getRowCount() - 1;
        for (int i = filas; i >= 0; i--) {
            edicion.removeRow(i);

        }
    }
