    private void controlAcceso(){
        int valor=SelectAcceso.acceso.getElGrupo();
        switch (valor) {
            case 1:
                nivel_1=true;
                break;
            case 2:
                nivel_2=true;
                break;
            default:
                nivel_3=true;
                break;
        }
        jmItemAdminAlumnos.setEnabled(false);
        jmItemAdminMaterias.setEnabled(false);
        jmItemBajaAlmuno.setEnabled(false);
        jmItemUsuarios.setEnabled(false);
        jmItemGestorAlumno.setEnabled(false);
    }
    
    public void permisosMenu(){
        if(nivel_3==true){
            jmItemAdminAlumnos.setEnabled(true);
        }else if(nivel_2==true){
            jmItemAdminAlumnos.setEnabled(true);
            jmItemAdminMaterias.setEnabled(true);
        }else {
            jmItemAdminAlumnos.setEnabled(true);
            jmItemAdminMaterias.setEnabled(true);
            jmItemBajaAlmuno.setEnabled(true);
            jmItemUsuarios.setEnabled(true);
            jmItemGestorAlumno.setEnabled(true);
        }
    }

